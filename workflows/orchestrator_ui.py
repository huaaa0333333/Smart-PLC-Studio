# ==========================================
#  進度指示器
# ==========================================
def _render_progress(step):
    step_num = {"panel_done": 1, "arch_done": 2, "scl_done": 3, "all_done": 4}.get(step, 0)
    labels = ["① 控制箱工程", "② 架構設計", "③ SCL 生成", "④ HMI 設計"]
    cols = st.columns(4)
    for i, (col, label) in enumerate(zip(cols, labels)):
        with col:
            if i < step_num:
                st.markdown(
                    f"<div style='text-align:center;padding:10px;"
                    f"background:linear-gradient(135deg,#1b5e20,#388e3c);"
                    f"color:white;border-radius:10px;font-weight:bold;'>"
                    f"✅ {label}</div>",
                    unsafe_allow_html=True,
                )
            elif i == step_num and step != "all_done":
                st.markdown(
                    f"<div style='text-align:center;padding:10px;"
                    f"background:linear-gradient(135deg,#e65100,#ff6d00);"
                    f"color:white;border-radius:10px;font-weight:bold;'>"
                    f"👁️ {label} — 審核中</div>",
                    unsafe_allow_html=True,
                )
            else:
                st.markdown(
                    f"<div style='text-align:center;padding:10px;"
                    f"background:#424242;color:#888;border-radius:10px;'>"
                    f"⬜ {label}</div>",
                    unsafe_allow_html=True,
                )


