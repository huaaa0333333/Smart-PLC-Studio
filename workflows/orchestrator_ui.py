def _execute_tia_deploy(pipeline_res, scl_data, is_create_mode, tia_plc):
    """實際執行 TIA Portal Openness 寫入"""
    if not tia_plc.strip():
        st.warning("請填寫目標 PLC 名稱。")
        return
    if not is_create_mode and not st.session_state.global_tia_proj.strip():
        st.warning("請填寫完整的專案路徑。")
        return
    if is_create_mode and (not st.session_state.global_tia_dir.strip() or not st.session_state.global_tia_new_name.strip()):
        st.warning("請填寫目標存放資料夾與新專案名稱。")
        return

    with st.spinner("正在與 TIA Portal 建立通訊，並在背景執行匯入 (約需 30~60 秒)..."):
        temp_scl_path = os.path.abspath("temp_export.scl")
        temp_tags_path = os.path.abspath("temp_tags.tsv")
        try:
            clean_code = scl_data['clean_scl'].replace('\x00', '').replace('$00', '16#00')
            with open(temp_scl_path, "w", encoding="utf-8") as f:
                f.write(clean_code)

            # TSV 變數表
            try:
                df = pd.read_csv(
                    io.StringIO(scl_data['res'].csv_tags),
                    dtype=str, skipinitialspace=True, on_bad_lines='skip',
                )
                df = df.fillna("")
                if not df.empty:
                    df.to_csv(temp_tags_path, sep='\t', index=False, encoding="utf-8-sig")
            except Exception as e:
                st.warning(f"變數表匯出警告: {e}")
                temp_tags_path = None

            exporter = TIAExporter()

            if is_create_mode:
                # 從 AI 架構師選型結果查 catalog 取 OrderNumber
                _plc_catalog = {}
                try:
                    with open("data/plc_catalog.json", "r", encoding="utf-8") as _f:
                        _plc_catalog = json.load(_f)
                except Exception:
                    pass
                ai_model_key = pipeline_res["arch"].plc_catalog_selection if pipeline_res.get("arch") else ""
                mlfb_str = _plc_catalog.get(ai_model_key, "")
                if not mlfb_str:
                    st.warning(f"⚠️ AI 選擇的型號 '{ai_model_key}' 不在型錄中，將嘗試以預設方式建立。")
                res = exporter.import_scl_to_tia(
                    project_path_or_dir=st.session_state.global_tia_dir,
                    scl_path=temp_scl_path,
                    plc_name=tia_plc,
                    tag_tsv_path=temp_tags_path,
                    is_create_mode=True,
                    project_name=st.session_state.global_tia_new_name,
                    mlfb_string=mlfb_str,
                )
            else:
                res = exporter.import_scl_to_tia(
                    project_path_or_dir=st.session_state.global_tia_proj,
                    scl_path=temp_scl_path,
                    plc_name=tia_plc,
                    tag_tsv_path=temp_tags_path,
                    is_create_mode=False,
                )

            if res["success"]:
                st.success(f"🎉 {res['message']}")
                with st.expander("查看 TIA 背景執行紀錄"):
                    st.code(res['details'], language="text")
            else:
                st.error(f"❌ {res['message']}")
                if res.get('error'):
                    st.error(f"錯誤原因:\n```text\n{res['error']}\n```")
                if res.get('details'):
                    with st.expander("查看 TIA 背景執行紀錄"):
                        st.code(res['details'], language="text")
        finally:
            if os.path.exists(temp_scl_path):
                os.remove(temp_scl_path)
            if temp_tags_path and os.path.exists(temp_tags_path):
                os.remove(temp_tags_path)


